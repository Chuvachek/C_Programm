#include <stdio.h> 

/* СМЫСЛ ПРОГРАММЫ: Мы изучаем работу с массивами и строками в Си.
   Программа читает строки из консоли одна за другой и в конце 
   печатает самую длинную из них.
*/

#define MAXLINE 1000 /* Максимальный размер выделенной памяти под строку */

/* Прототипы функций (анонсы для компилятора) */
int getline(char line[], int maxline); /* Читает текст с клавиатуры в массив, возвращает его длину */
void copy(char to[], char from[]);    /* Копирует символы ИЗ массива 'from' В массив 'to' */

int main()
{
    int len;               /* Длина строки, которую пользователь ввел прямо сейчас */
    int max;               /* Рекордная длина: максимальная из всех просмотренных строк */ 
    char line[MAXLINE];    /* Буферный массив на 1000 элементов для текущей строки */
    char longest[MAXLINE]; /* Массив-сейф на 1000 элементов для хранения строки-рекордсмена */
    
    max = 0;               /* Изначально рекорд равен 0, так как ни одной строки еще не ввели */
    
    /* Цикл работает до тех пор, пока длина введенной строки больше нуля (пока не нажали Ctrl+D/Z) */
    while ((len = getline(line, MAXLINE)) > 0) {
        if (len > max) {             /* Если свежая строка оказалась длиннее нашего рекорда */
            max = len;               /* Обновляем числовой рекорд максимальной длины */
            copy(longest, line);     /* Физически переносим текст из line в сейф longest */
        }
    }
    
    if (max > 0) {                   /* Защита: была ли введена хотя бы одна строка? */
        printf("%s", longest);       /* %s — спецификатор для печати всей строки до нуль-терминатора */
    }
    
    return 0;                        /* Сигнал операционной системе об успешном завершении программы */
}

/* Определение getline: читает поток символов в массив s, контролируя лимит lim */ 
int getline(char s[], int lim) 
{
    int c, i; /* c — коробка для текущего символа, i — индекс (номер) текущей ячейки массива */
    
    /* Цикл считывает по одной букве за шаг. Условия продолжения:
       1) i < lim-1           — в массиве еще есть свободное место (оставляем 1 ячейку под '\0')
       2) (c = getchar()) != EOF — клавиатура активна, не нажат сигнал конца ввода (Ctrl+D / Ctrl+Z)
       3) c != '\n'           — текущий считанный символ не является клавишей Enter
    */
    for (i = 0; i < lim-1 && (c = getchar()) != EOF && c != '\n'; ++i) {
        s[i] = c; /* Если все условия верны, записываем пойманную букву в текущую ячейку массива */
    }
        
    /* Исправлено: Если цикл остановился именно из-за нажатия Enter ('\n'), 
       то сам символ Enter уже лежит в переменной 'c', но еще не попал в массив.
       Мы насильно сохраняем строку вместе с нажатым Enter на конце.
    */
    if (c == '\n') { 
        s[i] = c; /* Записываем символ Enter в массив */
        ++i;      /* Сдвигаем индекс вперед, освобождая место для финала */
    }
    
    s[i] = '\0';  /* Ставим скрытый нуль-терминатор (знак СТОП). Его пишет сам компьютер в памяти */ 
    return i;     /* Возвращаем в main итоговое количество считанных символов */
}

/* Определение copy: копирует массив посимвольно, пока не встретит знак СТОП */ 
void copy(char to[], char from[])
{
    int i;
    i = 0;
    
    /* Копируем символ из 'from' в 'to' и проверяем: не скопировали ли мы случайно '\0'? */
    while ((to[i] = from[i]) != '\0') {
        ++i; /* Переходим к следующей ячейке */
    }
}